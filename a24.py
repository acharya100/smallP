# tuple

fruits = {"apple","mango","banana","orange","pineapple","watermelon"}

print(fruits)

fruits.add("amala")
print(fruits)

fruits.remove("pineapple")
print(fruits)

fruits.pop()
print(fruits)



# sets

class_11 = {'computer','nepali','english','physics','chemistry','arts'}
class_12 = {'computer','nepali','english','math', 'biology'}

print(class_11.union(class_12))
print(class_11.intersection(class_12))
print(class_11.difference(class_12))
print(class_12.difference(class_11))